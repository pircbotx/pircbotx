package org.pircbotx.events;

import lombok.Data;
import lombok.EqualsAndHashCode;
import org.pircbotx.Channel;
import org.pircbotx.PircBotX;
import org.pircbotx.User;
import org.pircbotx.hooks.Event;

/**
 * Called whenever an ACTION is sent from a user.  E.g.
 * such events generated by typing "/me goes shopping" in most IRC clients.
 * @author Leon Blakey <lord.quackstar at gmail.com>
 */
@Data
@EqualsAndHashCode(callSuper = true)
public class ActionEvent extends Event {
	protected final User source;
	protected final Channel target;
	protected final String action;

	/**
	 * Default constructor to setup object. Timestamp is automatically set
	 * to current time as reported by {@link System#currentTimeMillis() }
	 * @param source The user that sent the message
	 * @param target The target channel of the action. A value of <code>null</code>
	 *               means the target is us.
	 * @param action The action carried out by the user.
	 */
	public <T extends PircBotX> ActionEvent(T bot, User source, Channel target, String action) {
		super(bot);
		this.source = source;
		this.target = target;
		this.action = action;
	}
}
